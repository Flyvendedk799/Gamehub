/**
 * may9 Phase 14 — eval-report markdown builder.
 *
 * Takes an EvalReport and produces the markdown the runner writes
 * into `evals/runs/{date}.md`. Pure compute so the runner stays
 * environment-agnostic (no Node fs in this module).
 */
import type { EvalReport, EvalResult } from './fixture.js';

function fmtTokens(n: number): string {
  if (n >= 1_000_000) return `${(n / 1_000_000).toFixed(2)}M`;
  if (n >= 1_000) return `${(n / 1_000).toFixed(1)}K`;
  return String(n);
}

function fmtPct(n: number): string {
  return `${(n * 100).toFixed(1)}%`;
}

function statusBadge(pass: boolean): string {
  return pass ? '✓ pass' : '✗ FAIL';
}

function fixtureRow(r: EvalResult): string {
  const o = r.observed;
  return `| ${statusBadge(r.pass)} | \`${r.fixture.slug}\` | ${o.engine ?? '—'} | ${fmtTokens(o.inputTokens)} | ${fmtPct(o.cacheHitRate)} | ${o.setTodosCalls} | ${o.validateGameSceneCalls}/${o.playtestGameCalls} | ${o.renderPreviewCalls} | ${o.strReplaceCalls} | ${o.audioCalls} | ${o.snapshotCount} | ${o.correctionCount} |`;
}

function failureBlock(r: EvalResult): string {
  if (r.pass) return '';
  const lines = [`### ✗ \`${r.fixture.slug}\` — ${r.fixture.name}`, ''];
  lines.push(`> ${r.fixture.description}`);
  lines.push('');
  lines.push('**Failures:**');
  for (const f of r.failures) lines.push(`- ${f}`);
  lines.push('');
  return lines.join('\n');
}

export function renderEvalReport(report: EvalReport): string {
  const out: string[] = [];
  out.push(`# Eval report — ${report.generatedAt}`);
  out.push('');
  if (report.baselineRef !== undefined) {
    out.push(`Generated by \`pnpm eval:games\`. Baseline reference: \`${report.baselineRef}\`.`);
  } else {
    out.push('Generated by `pnpm eval:games`.');
  }
  out.push('');
  out.push(
    `## Summary — ${report.summary.passed} / ${report.summary.total} fixtures passed (${report.summary.failed} failed)`,
  );
  out.push('');

  // Header row.
  out.push(
    '| status | fixture | engine | input tokens | cache hit | set_todos | validate/playtest | render_preview | str_replace | audio | snapshots | corrections |',
  );
  out.push('|---|---|---|---:|---:|---:|---:|---:|---:|---:|---:|---:|');
  for (const r of report.results) out.push(fixtureRow(r));
  out.push('');

  // Per-failure detail blocks.
  const failed = report.results.filter((r) => !r.pass);
  if (failed.length > 0) {
    out.push('## Failures');
    out.push('');
    for (const r of failed) out.push(failureBlock(r));
  } else {
    out.push('## Failures');
    out.push('');
    out.push('_(none)_');
    out.push('');
  }

  out.push('## How to read this report');
  out.push('');
  out.push(
    '- The `validate/playtest` column shows the count of `validate_game_scene` and `playtest_game` calls. The Phase 9b mandatory pre-done gate requires both to be ≥ 1.',
  );
  out.push(
    '- The `set_todos` cap is 12/design (Phase 9b); fixtures use a tighter 8 by default to leave headroom.',
  );
  out.push(
    '- The `render_preview` column should always be 0 in game mode (Phase 9b retired the tool there).',
  );
  out.push(
    '- `corrections` measures user-driven re-prompts beyond the initial brief. Brawler baseline: 6. Target: ≤ 2.',
  );
  out.push('');
  return out.join('\n');
}
